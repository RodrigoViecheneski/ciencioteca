- Requerer informações com o objetivo de armazenar em uma mídia
(arquivo)
<React-Native>
    GET - Pegar informação
    - Requisição para pegar a lista de contatos ou um contato especifíco
    POST - Envia informações / adiciona informações no sistema
    - Requisição para inserir um novo contato
    PUT - Atualização das informações
    - Requisição para atualizar informações de um contato
    -DELETE - Excluir informações
    - Requisição para excluir um contato.

